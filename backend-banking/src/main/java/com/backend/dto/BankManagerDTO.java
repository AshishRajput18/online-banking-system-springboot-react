package com.backend.dto;

public class BankManagerDTO {
    private Long id;      // Unique ID of the manager
    private String name;  // Full name of the manager
    private String email; // Optional, if you want to display

    // Constructor
    public BankManagerDTO(Long id, String name, String email) {
        this.id = id;
        this.name = name;
        this.email = email;
    }

    // Getters & Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
